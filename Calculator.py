num1=int(input("Enter your first number 1:-"))
num2=int(input("Enter your first number 2:-"))
opr=input("Enter the opr :-")
if opr=="+":
    print(num1+num2)
elif opr=="-":
    print(num1-num2)
elif opr=="*":
    print(num1*num2)
elif opr=="/":
    print(num1/num2)
else:
    print("invalid opr......")