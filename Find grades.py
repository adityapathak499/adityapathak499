grade = int(input("Enter you marks between 1 to 100 to check your grade:- "))

if grade >= 90 < 100:
    print("Your grade is Excellent")
elif grade >= 80 < 90:
    print("Your Grade is A")
elif grade >= 70 < 80:
    print("Your Grade is B")
elif grade >= 60 < 70:
    print("Your grade is C")
elif grade >= 50 < 60:
    print("Your grade is D")
else:
    print("You are fail....!")
